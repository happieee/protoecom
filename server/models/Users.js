module.exports =(sequelize, DataTypes)=>{

    const Users= sequelize.define("Users", {
                title:{
                    type: DataTypes.STRING,
                    allowNull: false,
                },

                postText:{
                    type: DataTypes.STRING,
                    allowNull: false,
                },

                username:{
                    type: DataTypes.STRING,
                    allowNull: false,
                }
    })
    
    return Users


}